
// Java Class
public class MethodsParametersTutorial {

	// Main method that allows the program to run
	public static void main(String[] args) {

		// Print statement
		System.out.println("Total sum:");

		// Printing with arguments
		System.out.println(calculator(5, 7, 2));

	}

	// Creating a method with parameters
	public static int calculator(int num1, int num2, int num3) {

		// Returns the sum of the arguments (5, 7, 2)
		return num1 + num2 + num3;

	}

} // End of class
